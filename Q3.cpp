// Q4. write a program for calculating volume of cuboid
 #include<iostream>
 using namespace std;
 int main(){
 int l,b,h,volume;
 cout<<"enter the length of cuboid l = :";
 cin>>l;
 cout<<"enter the breadth of cuboid b = :";
 cin>>b;
 cout<<"enter the height of cuboid h = :";
 cin>>h;
 volume =l*b*h;
 cout<<"volume of cuboid is = : "<<volume;
 return 0;
 }
